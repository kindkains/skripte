<h2>Was versteht man denn nun unter Schamanismus?</h2>

<p>Die Bezeichnung Schamane (shaman) stammt urspr&uuml;nglich aus Sibirien und bedeutet frei &uuml;bersetzt
soviel wie "spiritueller Heiler". Schamanische Praktiken, Rituale, Traditionen und teilweise auch
Ansichten unterscheiden sich je nach Region und dennoch haben alle Schamanen etwas gemeinsam. Der
Anthropologe Professor Michael Harner pr&auml;gte den Begriff des
<a href="http://de.wikipedia.org/wiki/Core-Schamanismus">"Core-Schamanismus"</a> (Kern-Schamanismus).
Michael Harner schlussfolgerte, dass alle Schamanen eine wesentliche Gemeinsamkeit haben, n&auml;mlich
das willentliche Erreichen eines ganz besonderen Bewu&szlig;tseinszustandes, der es dem Schamanen erlaubt
in die <a href="glossar.php#naw">"Nichtallt&auml;gliche Wirklichkeit"</a> zu reisen und dort Wissen und/oder
Kraft zum eigenen Nutzen oder f&uuml;r andere Menschen zu erwerben.</p>
<p>Wesentliche Elemente des Schamanismus sind:</p>
<ul>
<li>Trance</li>
<li>Ekstase</li>
<li>Heilung</li>
<li>ver&auml;nderte Bewu&szlig;tseinszust&auml;nde</li>
<li>Interaktion und Kommunikation mit Geistwesen</li>
<li>Seelenreisen (auch Geistreisen, Traumreisen oder schamanische Reisen genannt)</li>
</ul>

<img src="../../bilder/inka/Shaman.jpg" border="1" width="352" height="288" align="left" vspace="5" hspace="10"/>

<p>Blumiger ausgedr&uuml;ckt kann man sagen, dass Schamanen das Bindeglied zwischen unserer allt&auml;glichen
Welt und der Welt der Geister darstellen. Sie stehen mit einem Bein im Hier und Jetzt und mit dem
anderen in der Nichtallt&auml;glichen Wirklichkeit. Schamanen sind in der Lage mit Tieren und Pflanzen zu
sprechen, verstorbene Seelen ins Jenseits zu geleiten und anderen Menschen mit Hilfe der schamanischen
Reise und der Energiemedizin Unterst&uuml;tzung und Hilfe zu bieten. Wichtig dabei zu erw&auml;hnen ist allerdings,
dass nicht der Schamane der Heiler der Krankheit ist, sondern jeder Klient sich selbst heilt. Der
Schamane ist nur derjenige, der wei&szlig;, wie er die Selbstheilungskr&auml;fte anderer Menschen ansto&szlig;en und
aktivieren kann.</p>
<p>Schamanismus kann auch mit Religion verbunden werden, muss aber nicht. Viele Schamanen sind
gleichzeitig auch Animisten, d. h. sie glauben daran, dass alles auf der Welt von einem Spirit beseelt
ist: Die B&auml;ume, die Fl&uuml;sse, der Wind, die Steine... etc.</p>
Schamanismus an sich ist jedoch <i>keine</i> Religion, sondern eine Lebenseinstellung. Die Ziele
des Schamanen sind:
<br /><br /><br /><br />
<ul>
<li>Herstellung und Erhaltung von Harmonie und Balance</li>
<li>Sch&uuml;tzen und Beh&uuml;ten des "Garten Eden" (Mutter Erde)</li>
<li>&Uuml;bernahme der Verantwortung f&uuml;r die eigene Gesundheit</li>
<li>&Uuml;bernahme der Verantwortung f&uuml;r das eigene Leben (Freiheit, Selbstbestimmung)</li>
<li>Die Welt sich so zu "ertr&auml;umen", wie er sie gern an seine Kinder weitergeben m&ouml;chte</li>
</ul>

<p>In fr&uuml;heren Zeiten war der Schamane der Mittelpunkt der Gesellschaft. Er war daf&uuml;r verantwortlich, dass
sein Dorf gesund war und gedeihen konnte (Heilung), den J&auml;gern das n&ouml;tige Wissen um den Aufenthalt der
Beutetiere zu vermitteln (Vision, Information) und die Geister und G&ouml;tter fr&ouml;hlich zu stimmen, damit
sie den Menschen wohlgesonnen waren (Interaktion mit der Geisterwelt).</p>

