<h2>Wer ist Elizabeth Jenkins?</h2>

<a href="http://www.inka-online.com/"><img src="../../bilder/inka/Elizabeth.jpg" alt="Bild von Elizabeth Jenkins" title="Elizabeth Jenkins" width="155" height="212" align="left" vspace="5" hspace="8"/></a>

<p>Elizabeth B. Jenkins, ist eine ausgebildete Psychologin und Gr&uuml;nderin / Direktorin der
<a href="wiraqocha.php">"Wiraqocha Foundation"</a>, einer Gesellschaft f&uuml;r den Schutz und Erhalt Indigenen Wissens und
Weisheit. Au&szlig;erdem ist sie die Autorin der B&uuml;cher "Die R&uuml;ckkehr des Inka" (1997) und "Journey to Q'eros - Golden Cradle of the Inka"
(2004) und Mitwirkende beim "Shaman's Drum magazine". Ihr Buch "Die R&uuml;ckkehr des Inka" wurde 1997 zum internationalen Bestseller
und wurde in elf unterschiedlichen Sprachen &uuml;bersetzt. Sie finden das Buch auch unter dem Punkt <a href="literatur.php">Literatur</a>.</p>
<p>Elizabeth Jenkins ist auch eine begnadete S&auml;ngerin und Songschreiberin und hat erst k&uuml;rzlich ihre erste eigene CD "Inka Spirit"
aufgenommen. Alle St&uuml;cke der CD sprechen von ihren spirituellen und pers&ouml;nlichen Erlebnissen in und um Peru und den Anden.
Der Stil ist eine einmalige Mischung aus Latin Jazz, gespielt und untermalt mit traditionellen andinen Musikinstrumenten.</p>
<p>Elizabeth ist eine initiierte andine Priesterin der vierten Stufen und empfing die
<a href="glossar.php#hatun karpay">Hatun Karpay</a> von Don Juan Nunez del Prado.
Sie veranstaltet seit &uuml;ber 20 Jahren Initationsreisen (Hatun Karpay) nach Peru, zu den heiligen St&auml;tten der Inka, wo jeder zu
einem andinen Prieser der vierten Stufe aufsteigen kann.</p>
Dar&uuml;ber hinaus lehrt sie auf internationaler Ebene anderen Menschen den <a href="willkommen.php">"Inka-Schamanismus"</a> und ist eine gefragte Rednerin
&uuml;ber spirituelle Erfahrungen bei psychologischen, esoterischen, kreativen und spirituellen Gruppen und Kongressen.</p>

