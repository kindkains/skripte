<p>The Wiraqocha Foundation is a non-profit organization, and proceeds from workshops, trips, sales etc are used to directly
assist the Q'ero indians, the direct descendants of the Inkas, with such things as water supplies and schools, in the spirit
of Ayni - sacred reciprocity.</p>

<p>Wiraqocha is the Andean word for God in Quechua, the language of the Inkas.</p>

<p>The name comes from two Quechua words meaning oil (wira), and water (qocha). The Andean God, who reigned over the enormous
and diverse Inka empire, was considered so powerful that she/he could bring together two substances as disparate as oil and
water. We need this kind of power and wisdom today as we confront a world of extreme diversity at the brink of ecological,
political, and moral breakdown. We must remember the voices of the Ancient Ones.</p>

<p>In the Inka empire - an empire over nine million strong that knew no unemployment or hunger - the philosophy was one of
tolerance of diversity and harmony with nature. This is ancient widsom that we must reintegrate into today's society if
we are to survive this new millenium and create a truly sustainable and healthy global society. The prophets of the Andes,
the holy men and women who are the seers and visionaries of their people say that the current time period, 1993-2012, is
a "critical period" in the evolution of human consciousness. We have entered the time they call the Taripay Pacha, the
"Age of Meeting Ourselves Again." It is an epoch in which we humans must reconcile our relationship with Pachamama, Mother
Earth; and learn to truly share with our brothers and sisters.</p>

<p>The mission and purpose of the Wiraqocha Foundation is to promulgate a philosophy of life in harmony with the natural
world, a way of life preserved by the Q'ero Indians of Peru, direct descendants of the Inkas, and to do its utmost to
carry out the vision of the Andean prophets. The Foundation's first concern is to bring immediate relief to the Q'ero
people-a tribe of 400 Peruvian Indians on the verge of extinction-while preserving their traditional culture and identity.
The Foundation will also carry out projects in public education and awareness of the fundamental principals of the Andean
philosophy and how it works in our everyday lives.</p>

<p>The current goals and values of our consumer society are making us a danger not only to indigenous peoples all over the
world, but here in our own country and to our very own lives! We have much to learn from the Andean people, who say it is
now time to put away fear and come together for the common good, sharing our diverse cultural achievements in an atmosphere
of trust and collaboration. We need the wisdom of all peoples of the earth working together to achieve a sustainable
global community.</p>

<p>The Wiraqocha Foundation will provide a special link to the indigenous voice of the Q'ero people and work for the goals
and values of life in harmony with nature worldwide. The Foundation will facilitate the coming together of indigenous
voices from around the world to share ceremony, life philosophies and wisdom with each other and with health care, big
business, and government. The Wiraqocha Foundation will work to become a voice of inspiration and action for the values
of indigenous people within our current social, economic and political power structures. The Wiraqocha Foundation will
sponsor conferences and events to facilitate the interchange of knowledge in these areas. We cannot go back to the old
ways-but we can learn to build successfully on the ways our ancestors. We must go forward with heart and vision.</p>

