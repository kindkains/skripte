in der Sonnenh&ouml;hle unser drittes Nawi &ouml;ffnen und mit Vater Sonne verbinden.</p>
<p>Die letzte Station des zweiten Tages ist <a href="bilder.php#Ollantaytambo">Ollantaytambo.</a> Inmitten dieser v&ouml;llig
intakten, uralten Inka Siedlung, werden wir uns zur Windschleuse begeben und dort unser viertes Nawi &ouml;ffnen, um uns mit Wayra,
dem Geist des Windes zu verbinden.</p>

