<p><i><b>Erster Tag: Karpay Ayni und Samenk&ouml;rner der Inka</b></i></p>
<p>Wir beginnen das Ritual der Hatun Karpay auf der <a href="bilder.php#wakaypata">Wakaypata</a> in der Hauptkathedrale in Cuzco um "Inka Samenk&ouml;rner" zu werden
und reisen zu den heiligen St&auml;tten in Cuzco und der nahen Umgebung. In der Hauptkathedrale lernen wir uns mit dem g&ouml;ttlich
m&auml;nnlichen Energien des Kosmos (Jesus Christus) und mit den g&ouml;ttlich weiblichen Energien des Kosmos (Mutter Maria) zu verbinden.
Wir erfahren direkt und unmittelbar die Energien <b>hinter</b> den Symbolen wahrzunehmen. F&uuml;r einen andinen Priester spielt es
keine Rolle, ob er in eine katholische Kirche, eine muslimische Moschee, eine j&uuml;dische Synagoge oder sonst wo hingeht. F&uuml;r
einen Paqo z&auml;hlt nur die Energie hinter der jeweiligen Symbolik.</p>
<p>Die n&auml;chste Stationen sind die Ruinen von <a href="bilder.php#qenko">Q'enko</a>, wo wir auf der Illia Pata, der Plattform des Lichts, lernen werden unseren
Qosqo, unseren energetischen Magen, zu benutzen, um mit diesem schwere Energien (Hoocha) zu verspeisen. Hier werden auch zum
ersten Mal die Apus gerufen und ein gemeinsames Despacho hergestellt. Hier werden wir auch unser erstes <i>Karpay ayni</i>
vollziehen, den Austausch von pers&ouml;nlicher Kraft und erhalten die Einweihung in die Inkatradition und die Ahnenlinie der Inka.</p>
<p>Dann geht es zur Kultst&auml;tte nach Amaru Mach'ay, der Schlangenh&ouml;hle. Dort arbeiten wir mit den schweren Energien unserer
Vergangenheit und unserer Eltern und vollziehen das inkaische Ritual des "erwachsen werdens", indem wir uns energetisch von
unseren biologischen Eltern trennen und Mutter Erde und Vater Sonne als unsere wahren, kosmischen Eltern annehmen. Dies macht
uns dann zu Br&uuml;dern und Schwestern aller Menschen.</p>
<p>Das letzte Ritual des Tages findet an dem See statt, wo der letzte Inkaherrscher Huascar geboren wurde. Dort werden wir
unsere pers&ouml;nliche Kraft mit dem See austauschen und Huascar bitten unseren Energiek&ouml;rper mit der Macht des Inka anzuf&uuml;llen.
Anschlie&szlig;end erhalten wir unseren ersten <i>khuya</i>.</p>

