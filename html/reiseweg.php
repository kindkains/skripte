<h2>Hatun Karpay - Der Reiseweg - Unsere Wegstationen</h2>

<p>Wir werden zwei Tage vor Beginn der Hatun Karpay in Cuzco landen. Die ersten zwei Tage dienen dazu, sich von dem eventuell
auftretenden <a href="http://de.wikipedia.org/wiki/Jetlag">Jetlag</a> zu erholen und sich vor allem und in erster Linie zu
