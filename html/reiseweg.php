aus. W&auml;hrend der Hatun Karpay werden wir an diverse Orte gelangen, die sogar noch h&ouml;her liegen. Von daher ist es absolut notwendig
sich vorher an die d&uuml;nne Luft anzupassen. Anschlie&szlig;end startet die 10 t&auml;gige Hatun Karpay:</p>

