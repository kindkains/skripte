Inka gesto&szlig;en! Hier oben in den Anden, zum Teil in <b>&uuml;ber 5000 Meter H&ouml;he</b> hatten sie die letzten 500 Jahre unendeckt von den
spanischen Conquestadoren und der Kirche &uuml;berlebt und ihre Geschichten und ihr Wissen von Generation zu Generation m&uuml;ndlich
weiter gegeben.</p>

<p>Heute &ouml;ffnen sich die Qeros der westlichen Welt und teilen mit uns ihr &uuml;beraus reiches spirituelles Wissen. Dies ist vor allem
Oscar Nunez del Prado und seinem Sohn Juan zu verdanken. <a href="juan.php">Juan Nunez del Prado</a> trat in die Fu&szlig;stapfen
seines Vaters und wurde ebenfalls Professor f&uuml;r Anthropologie an der Universit&auml;t in Cuzco. Als absolut rational denkender
Anthropologe wollte er vor 40 Jahren eine Studie &uuml;ber die Spiritualit&auml;t der Inka starten und traf im Rahmen seiner Studien
auf den Inka Hohepriester Don Benito Qoriwaman. W&auml;hrend des Interviews dieses weisen und bekannten Meisters geschah etwas
ausgesprochen merkw&uuml;rdiges: Don Benito sprach pl&ouml;tzlich zu Juan in einer eigenartigen Sprache, welche Juan niemals zuvor
geh&ouml;rt hatte und die klare, dreidimensionale Bilder und pr&auml;zise Vorstellungen direkt in seinen Geist pflanzten. Noch merkw&uuml;rdiger
war es, da&szlig; Juan in genau derselben Sprache antwortete, ohne sie zu kennen. Und das merkw&uuml;rdigste von allem war, da&szlig; es ihm
absolut normal vorkam und &uuml;berhaupt nicht merkw&uuml;rdig!</p>

<p>Erst als Don Benito das Gespr&auml;ch beendete, Juan verabschiedete und der damals junge Anthropologe vor der T&uuml;r stand, d&auml;mmerte
ihm auf einmal, da&szlig; dort in dieser kleinen, &auml;rmlichen Holzh&uuml;tte, soeben etwas h&ouml;chst sonderbares geschehen war. Juan Nunez del
Prado stand vor einem Dilemma. Er, als absolut rational und wissenschaftlich gepr&auml;gter Mensch war soeben mit der spirituellen
