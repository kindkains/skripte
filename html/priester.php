einen Qero-Priester live bei seiner Arbeit erlebt hat, wird den feinen, aber dennoch merklichen Unterschied sehr schnell
verstehen.</p>

<p>Wir bieten in unregelm&auml;&szlig;igen Abst&auml;nden Workshops in diesem spirituellen Pfad an. Die Workshops sind aufgeteilt in Inka
Pfad 1, 2 und 3 und bauen jeweils aufeinander auf und f&uuml;hren zu immer fortgeschrittenen Techniken und Ritualen. Die Teilnehmer
erhalten hierbei energetische Einweihungen in die Inka-Tradition und die Ahnenlinie und werden den Workshop als vollwertige
<a href="glossar.php#paqo"><i>Paqos</i></a> verlassen, mit allen Rechten und Pflichten. Gelegentlich bieten wir auch Workshops
mit unseren Lehrern <a href="juan.php">Don Nunez del Prado</a> und <a href="elizabeth.php">Elizabeth Jenkins</a> an.</p>

<p>Wir haben uns so arrangiert, da&szlig; egal bei welchen Lehrer man den Workshop absolviert, sei es bei Elizabeth, bei Juan oder
bei uns (Sandro und Andre), man die gleiche qualitative Ausbildung erh&auml;lt und dennoch bei jedem Lehrer bestimmte Dinge erlernen
kann, die man nur bei ihm oder ihr erh&auml;lt. So ist es m&ouml;glich bei jedem Lehrer einen Workshop zu absolvieren und <b>immer</b>
etwas neues dazu zu lernen und sein Wissen und seine F&auml;higkeiten zu vertiefen, aber man <b>mu&szlig;</b> nicht bei allen Lehrern
gewesen sein, um eine vollwertige Ausbildung zu erhalten und Paqo zu werden.</p>

<p>Ebenso gibt es die Option, den Workshop bei einem Lehrer zum halben Preis zu wiederholen und sich aufzufrischen, sollte man
bereits einmal den Workshop bei ihm oder ihr belegt haben. Vorkenntnisse sind f&uuml;r unsere Workshops nicht notwendig, da wir immer
