allem verstehen, ihm kein anderer Weg bleiben w&uuml;rde, als sie aus erster Hand zu erfahren. Und so wurde er zu Don Benito's
Sch&uuml;ler.</p>

<p>40 Jahre lang hat Juan Nunez del Prado unter den gr&ouml;&szlig;ten Inka Meistern seiner Zeit gelernt: Don Benito Qoriwaman, Don
Andres Espinoza, Don Melchior Deza und Don Manuel Q'espi. In all den Jahren hat er zusammen mit seinen Lehrern alles
