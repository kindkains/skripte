<p>In den f&uuml;nziger Jahren machte der Anthropologe Oscar Nunez del Prado eine sagenhafte Entdeckung. Er hatte sich mit seinem
Expeditionsteam in den peruanischen Anden verlaufen. Dabei stie&szlig; er eine eine Gruppe Indianer, welche einen merkw&uuml;rdigen
