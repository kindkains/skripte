<p>Heute sind wir in der Lage dieses Wissen in praktischen Ausbildungen jedem Menschen zug&auml;nglich zu machen und es ist auch
unsere Pflicht es zu verbreiten, um die <a href="prophezeihung.php">Prophezeihungen der Inka</a> von einem goldenen Zeitalter
