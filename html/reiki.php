<p>Die Reikikraft f&ouml;rdert den nat&uuml;rlichen Selbstheilungsprozess auf allen Ebenen und unterst&uuml;tzt den Empf&auml;nger in der f&uuml;r ihn
angemessenen Form. (F&uuml;r den einen kann dies Entspannung sein und f&uuml;r den anderen mehr Energie und Tatkraft.) Der freie Wille
