<h2>Gendai Reiki H&ocirc;</h2>

<p><b>Der Begriff  "Reiki"</b></p>

<p>Das Wort stammt aus dem Japanischen und wird im Westen als “Ree Ki” ausgesprochen. H&auml;ufig wird es hier im Westen mit
"Universelle Lebensenergie" &uuml;bersetzt. Dies ist allerdings nicht ganz korrekt. In Japan bezeichnet es den Spirit/Gott,
spirituelle Gebiete und zum Teil auch Heilung. REI = Spirit/Gott; KI = Lebensenergie;  REIKI = Gott f&uuml;hrt Lebens- / Heilungsenergie.
Die &Uuml;bersetzung als universelle Lebensenergie ist aber insofern sinnvoll, als sie deutlich macht, dass es keine Religion ist
und auch Atheisten damit arbeiten k&ouml;nnen.

Die Bezeichnung “Reiki” wird h&auml;ufig sowohl f&uuml;r die Energie selbst, als auch f&uuml;r eine von Mikao Usui vermittelte Methode, mit
dieser Energie umzugehen, verwendet. (F&uuml;r diese Methode gibt es auch noch die Bezeichnung "Usui Shiki Ryoho" oder "Das
Usui-System der nat&uuml;rlichen Heilung".).

Das Wort “Reiki” gab es schon Jahrhunderte vor Usui. In Japan ist das Wort sehr bekannt, aber das Usui-Reiki ist weniger
bekannt. Das Usui-Reiki ist ein Teil von Reiki. Es gibt neben dem Usui-Reikisystem auch andere Reikisysteme, die unter dem
Begriff ““Reiki” laufen. Im folgenden beziehen wir uns (soweit nichts anderes angegeben ist) auf das Usui-Reiki.</p>

<p><b>Was ist Reiki?</b></p>

<p>Reiki = eine feinstoffliche, intelligente Energie - die Essenz allen Lebens. Es ist alle Energie, die von Gott (einer
h&ouml;heren Macht, wie immer man sie nennen will) gef&uuml;hrt wird. Es gibt auch Energie-/Heilungssysteme, bei denen die Energie mit
dem eigenen Geist/Willen gef&uuml;hrt wird und der Erfolg von den F&auml;higkeiten des Heilers abh&auml;ngig ist. Diese F&auml;higkeiten k&ouml;nnen
