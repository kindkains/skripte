<h2>Die Wahre Geschichte des Mikao Usui</h2>

<img src="../../bilder/inka/mikaousui.jpg" width="400" height="515" align="right" vspace="5" hspace="10" alt="Mikao Usui"/>

<p>&Uuml;ber Mikao Usui und die Gr&uuml;ndung seiner sehr bekannten und beliebten Reiki-Methode sind allerhand Geschichten und Ger&uuml;chte
im Umlauf, von denen einige jedoch an den Haaren herbeigezogen sind. Man findet im Internet auch die verschiedensten Aussagen.
Warum also sollte gerade diese Geschichte, der Wahrheit n&auml;her sein, als alle anderen?
Ganz einfach, eine meiner Reiki-Lehrer, Hiroko Kasahara, ist geb&uuml;rtige Japanerin und sie hat sich die M&uuml;he gemacht, die
japanische Inschrift auf Mikao Usui's Gedenkstein neben seinem Grab ins Deutsche zu &uuml;bersetzen. Von allen m&ouml;glichen Quellen
