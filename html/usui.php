am n&auml;chsten. Ich werde hier nun den Text abtippen, welche meine Lehrer Hiroko und Josef in ihrer Reiki-Mappe an ihre Sch&uuml;ler
weitergeben:</p>

<p><b>Geschichte der Reiki-Methode und ihr Gr&uuml;nder Mikao Usui</b></p>

<p>Von Usui-Sensei*1 wird oft f&auml;lschlich behauptet, er sei Professor der Doshisha-Universit&auml;t oder ein christlicher Priester
gewesen. Diese Legende entspricht nicht den Tatsachen. Oft wird er Dr. Usui genannt, aber er war nicht promoviert. &Uuml;ber die
Geschichte des Reiki und Usui-Senseis Leben und Usui-Senseis Nachfolger gibt es zahlreiche erfundene Erz&auml;hlungen. Vermutlich
wurden diese ben&ouml;tigt, um damals, direkt nach dem Krieg zwischen Japan und den USA, Reiki in den USA verbreiten zu k&ouml;nnen.
Die wahre Geschichte von Usui-Senseis Leben ist jedoch auf dem Gedenkstein f&uuml;r Usui-Sensei, der neben seinem Grab aufgestellt
wurde, verzeichnet.</p>

<p>Usui-Sensei wurde am 15. August 1865 in einem kleinen Dorf der Pr&auml;fektur Gifu in Japan geboren. Schon als Kind lernte er sehr
flei&szlig;ig, so da&szlig; seine F&auml;higkeiten denen seiner Altersgenossen weit &uuml;berlegen waren. Er studierte in Europa, Amerika und China,
er hat aber nie promoviert. Trotz seiner besonderen F&auml;higkeiten hatte er beruflich nicht viel Erfolg. Er hatte oft Pech und
finanzielle Schwierigkeiten. Er lebte sozusagen als ein freier Mensch. So zu leben war zu seiner Zeit in Japan viel schwieriger
als heute. Er war warmherzig und bescheiden, legte wenig Wert auf &Auml;u&szlig;erlichkeiten. Er war von kr&auml;ftiger Statur und l&auml;chelte
immer. Aber wenn es darauf ankam, bewies er Willensst&auml;rke. Er war sehr geduldig und bedachtsam. Er las gern und war auf den
verschiedensten Gebieten beschlagen, so z.B. in Geschichte, Medizin, Buddhismus, Christentum, Psychologie, Parapsychologie,
Orakelwesen und Physiognomik. Sein weiter geistiger Horizont und sein unerm&uuml;dliches Streben waren die Basis f&uuml;r die
Entwicklung der Reiki-Methode.</p>

<p>Usui-Sensei &uuml;bte verschiedene Berufe aus, wie z.B. als Beamter, Angestellter, Unternehmer, Journalist, Sekret&auml;r eines Politikers,
Missionar einer Religionsgemeinschaft, Erzieher f&uuml;r Str&auml;flinge. Indem er auf diese Weise Erfahrungen sammelte, die Schauseite
und Abseite der Gesellschaft erlebte, sah er sich eines Tages mit der Frage konfrontiert: Was ist der Sinn des Lebens.?
Durch seine unerm&uuml;dliche Suche nach Wahrheit gelangte er zu der Antwort, dass das Leben letztendlich darauf zielt, ein
Urvertrauen zum Universum und die unersch&uuml;tterliche Ruhe der Seele zu gewinnen. Das hei&szlig;t, zu begreifen, da&szlig; wir zwar unser
Bestes tun m&uuml;ssen, aber das, was wir trotzdem nicht &auml;ndern k&ouml;nnen, dem Universum &uuml;berlassen m&uuml;ssen und zu einem Zustand der
Sorglosigkeit oder des Urvertrauens gelangen m&uuml;ssen.</p>

<p>Der Autor des Buches "Die moderne Reiki Methode zur Heilung", Hiroshi Doi*2 schreibt, da&szlig; bei Usui Sensei dieses Begreifen
als die erste Stufe der Erleuchtung angesehen wird. Die Erleuchtung habe verschiedene Stufen. In der ersten Stufe gelange man
rational zu dieser Erkenntnis, das ist die Erkenntnis auf mentaler Ebene. Wahre Erleuchtung hei&szlig;e aber, durch ein h&ouml;heres
Selbst die Wahrheit intuitiv zu erkennen und durch die &Uuml;bereinstimmung mit dieser Wahrehit zur ewigen Freude zu gelangen.
Usui-Sensei hat dies zwar verstanden, war aber noch nciht erleuchtet. Um zur Erleuchtung zu gelangen, trat er in einen
Zen-Tempel ein und &uuml;bte drei Jahre Zen aus. Trotzdem konnte er nicht zur Erleuchtung gelangen. Verzweifelt fragte er einen
Zen-Meister: "Wie kann ich die Erleuchtung erlangen?" Der Meister antwortete darauf spontan: "Dann strerben Sie einmal."
Usui-Sensei dachte, sein Leben w&auml;re zu Ende. Entschlossen begann er, auf dem Kurama-Berg*3 zu faasten. Das war im M&auml;rz 1922.
Er war damals 57 Jahre alt.</p>

<p>Am 21. Tag seines Fastens sp&uuml;rte er um Mitternacht einen heftigen Sto&szlig;, als ob ein Donnerschlag ihn auf den Kopf getroffen
h&auml;tte. Er verlor sein Bewu&szlig;tsein. Als er wieder zu sich kam, wu&szlig;te er nicht, wie viele Stunden er bewu&szlig;tlos gewesen war. Es
war gerade bei Sonnenaufgang. Es war ein Erwachen, das er noch nie zuvor erlebt hatte. Er f&uuml;hlte, da&szlig; sein K&ouml;rper und seine
Seele vollkommen erfrischt waren. Mit dem hefitgen Schlag, der ihn auf den Kopf getroffen hatte, war die Energie des Universums
in seinen K&ouml;rper und seine Seele gedrungen. Die Energie des Universums und seine eigene Energie schwangen ineinander.
Usui-Sensei gelangte zur Einheit mit dem Universum: Er war erleuchtet.</p>

<p>Als er voller Freude den Berg hinunterstieg, stolperte er &uuml;ber einen Stein. Ein Zehennagel war abgerissen. Unwillk&uuml;rlich
legte er seine Hand auf die Wunde. Sein Schmerz verschwand sofort und seine Wunde war geheilt. Zun&auml;chst probierte er diese
heilende F&auml;higkeit am eigenen K&ouml;rper und bei seiner Familie aus, und er stellte fest, da&szlig; die Wirkung sofort eintrat.
Danach entwickelte er die Reiki-Methode und eine Methode, diese F&auml;higkeit auf andere zu &uuml;bertragen. Er sagte: "Es w&auml;re nicht
gut, diese F&auml;higkeit f&uuml;r sich zu behalten. Wir m&uuml;ssen sie unseren Mitmenschen weitergeben und die Freude mit ihnen teilen."</p>

<img src="../../bilder/inka/kurama.jpg" width="596" height="400" align="left" vspace="5" hspace="10" alt="Kurama Tempel"/>

<p>Im April 1922 (d.h. ca. einen Monat nach dem Erleuchtungserlebnis auf dem Kurama-Berg) zog er nach T&ocirc;ky&ocirc; um und gr&uuml;ndete dort
die "Gesellschaft f&uuml;r die Usui-Reiki-Heilmethode" (Usui Reiki Ry&ocirc;h&ocirc; Gakkai)*4. Er unterrichtete und behandelte viele Leute,
und vor der T&uuml;r seiner Praxis stand immer eine sehr lange Schlange. Im September 1923 ereignete sich das gro&szlig;e Kant&ocirc;-Erdbeben
in T&ocirc;ky&ocirc; und Umgebung. Die Stadt brannte fast vollst&auml;ndig nieder, da die H&auml;user damals fast alle aus Holz gebaut waren.
Usui-Sensei behandelte und heilte unz&auml;hlige Menschen. Sein Ruf wurde immer gr&ouml;&szlig;er und gr&ouml;&szlig;er. Usui-Sensei wurde in ganz Japan
eingeladen, zu unterrichten und zu behandeln. Auf einer solchen Reise erkrankte er und starb am 9. M&auml;rz 1926 im Alter von 61
Jahren. (Das war vier Jahre nach seiner Erleuchtung auf dem Kurama-Berg.)</p>

<p>Usui-Sensei &uuml;bte insgesamt vier Jahre Reiki aus. In diesen vier Jahren hatte er ca. 2000 Sch&uuml;ler. Er soll etwa 21 Sch&uuml;ler zum
Meister-Grad (shinpi-den)*5 geweiht haben. Von diesen wurden bis jetzt nur elf Sch&uuml;ler identifiziert. Unter diesen elf sind
drei Frauen. Von Usui-Sensei und seinen Sch&uuml;lern sollen &uuml;ber eine Million Menschen behndelt und geheilt worden sein.
Usui-Sensei sah das Ziel der Reiki-Heilmethode nicht nur darin, Krankheiten zu behandeln und zu heilen, sondern auch darin,
das Bewu&szlig;tsein zu erweitern und sich spirituell zu entwickeln, die Gesundheit der Seele und des K&ouml;rpers zu erhalten und
schlie&szlig;lich das Gl&uuml;ck des Lebens zu finden und zu genie&szlig;en. Hierf&uuml;r lehrte er "Die f&uuml;nf Lebensregeln." Diese nannte er
"Wertvolle Methode zur Gl&uuml;cksfindung und Allheilmittel gegen alle Krankheiten." Seine Lebensregeln lauten: "Gerade heute
&auml;rgere dich nicht, sorge dich nicht, sei dankbar, arbeite hart, sei freundlich zu deinen Mitmenschen."*6 Er empfahl seinen
Sch&uuml;lern, morgens und abens mit gefalteten H&auml;nden diese f&uuml;nf Lebensregeln zu rezitieren und sie t&auml;glich in die Tat umzusetzen.</p>

<p>Usui-Sensei hinterlie&szlig; ein Handbuch der Reiki-Methode. In diesem Handbuch sind Usui-Senseis Lehrreden aus seinen Seminaren
von seinen engsten Sch&uuml;lern zusammengefasst worden und auch Usui-Sensei hat teilweise selbst geschrieben. Hier wird Reiki
in Form von Fragen und Antworten erkl&auml;rt, und gerade diesen Teil, "Fragen und Antworten" soll Usui-Sensei selbst verfa&szlig;t
haben. Eine Frage lautet: "Aus welchem Grund hat die Usui-Reiki-Heilmethode eine solch heilende Wirkung?" Usui-Sensei antwortet
darauf: "Diese Methode wurde mir von niemanden &uuml;bermittelt. Und ich habe nie geforscht, um diese heilende F&auml;higkeit zu
erlangen. Sondern beim Fasten bin ich auf mystische Weise mit der Energie des Universums in Ber&uuml;hrung gekommen und habe
zuf&auml;llig selber gemerkt, da&szlig; ich diese F&auml;higkeit erhalten habe. Ich bin zwar der Gr&uuml;nder dieser Methode, aber ich kann nicht
erkl&auml;ren, warum sie wirkt. Die heutige Wissenschaft kann das kaum ergr&uuml;nden, aber ich glaube, da&szlig; diese Methode in Zukunft
eines Tages mit der Wissenschaft zusammengehen wird."</p>

<p>Es gibt eine Legende um Usui-Sensei, er sei von einem Sch&uuml;ler in einer christlichen Schule nach Heilmethoden gefragt worden,
habe daraufhin in vielen B&uuml;chern danach geforscht und habe schlie&szlig;lich in einem in Sanskrit geschriebenen S&ucirc;tra von dieser
Methode mit Darstellung von Symbolen erfahren, und er habe am 21. Tag seines Fastens eine Licht-Erscheinung dieser Symbole
gesehen. Diese Legende kann nicht stimmen, denn Usui-Sensei selber sagt, da&szlig; er diese F&auml;higkeit zuf&auml;llig erhielt und zuf&auml;llig
selber entdeckte, da&szlig; er sie erhalten hatte. Au&szlig;erdem war er nie Lehrer oder Priester in einer christlichen Schule.</p>

<p>Usui-Sensei benutzte weder Symbole noch Mantras. Als er aber sah, da&szlig; einige seiner Sch&uuml;ler Schwierigkeiten hatten,
Fern-Reiki zu praktizieren, kam er darauf, Symbole und Mantras als Hilfmittel f&uuml;r Anf&auml;nger einzusetzen. Die Symbole &uuml;bernahm
er teilweise aus dem Buddhismus und teilweise aus dem Shint&ocirc;ismus des Altertums. Hiroshi Doi erkl&auml;rt, die Symbole und Mantras
solle man vergleichsweise als St&uuml;tzr&auml;der eines Fahrrades und nicht als die Hauptr&auml;der verstehen. Es sei wichtig, am Anfang
viel zu &uuml;ben und dabei Symbole und Mantras einzusetzen. Aber wenn man gen&uuml;gend Erfahrungen gesammelt hat und sich damit sicher
f&uuml;hlt, solle man anfangen, eine Stufe h&ouml;her zu gehen; d.h. man solle der jeweiligen eigenen Entwicklung entsprechend von den
Symbolen und Mantras ablassen und sich ohne Anwendung dieser Symbole und Mantras direkt mit der jeweiligen Energieschwingung
verbinden (d.h. ohne St&uuml;tzr&auml;der nur mit den Hauptr&auml;dern fahren). Die Symbole sind ein sehr wertvolles Hilfsmittel, aber man
solle sie nicht heiligen oder anbeten.</p>

<p>Auch die oft m&auml;rchenhaft erz&auml;hlte Geschichte, Usui-Sensei habe sieben Jahre lang in einem Armenviertel in Ky&ocirc;to kostenlos die
Obdachlosen behandelt, kann nicht stimmen. Denn er hat im M&auml;rz 1922 gefastet und die F&auml;higkeit des Heilens erhalten un schon
einen Monat sp&auml;ter gr&uuml;ndete er in T&ocirc;ky&ocirc; seine Gesellschaft und &uuml;bte dort seine volle T&auml;tigkeit aus. Au&szlig;erdem &uuml;bte er bis zu
seinem Tod insgesamt nur vier Jahre lang Reiki aus.</p>

