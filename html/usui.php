<p>Dr. Ch&ucirc;jir&ocirc; Hayashi (1879-1940) war Marinearzt und Oberst bei der Marine. Mehrere direkte Nachfolger Usui-Senseis waren
Marineoffiziere. Man vermutet, da&szlig; Reiki bei der Marine sehr verbreitet war. Dr. Hayashi erhielt im Jahr 1925 von Usui-Sensei
die Mesiter-Weihung und er&ouml;ffnete im gleichen Jahr eine Praxis in T&ocirc;ky&ocirc;. Dort standen acht Behandlungstische und arbeiteten
16 Behandler. Einen Klienten haben zwei Behandler behandelt. Die Praxis soll sehr floriert haben. Als Usui-Sensei im folgenden
Jahr starb, verlie&szlig; Dr. Hayashi dei Gesellschaft f&uuml;r die Usui-Reiki-Heilmethode. Nach Usui-Senseis Tod verlie&szlig;en viele seiner
Sch&uuml;ler diese Gesellschaft und gr&uuml;ndeten eigene Reiki-Gesellschaften. Dr. Hayashi gr&uuml;ndete das Hayashi-Reiki-Institut und
entwickelte auf der Basis von Usui-Reiki ein eigenes System mit Theorie und Technik. Er soll u.a. die Einweihungsmethode
Usui-Senseis ziemlich ver&auml;ndert und weiterentwickelt haben. Und diese von ihm ver&auml;nderte und weiterentwickelte
Einweihungsmethode wurde &uuml;ber Frau Takata in den Westen &uuml;bermittelt. Dr. Hayashi hat insgesamt dreizehn Sch&uuml;ler zum Meister
geweiht, darunter zwei Frauen, n&auml;mlich Frau Hayashi (seine Ehefrau) und Hawayo Takata. Nach seinem Tod &uuml;bernahm Frau Hayashi
das Hayashi-Reiki-Institut. Aber nach ihrem Tod konnte niemand das Institut &uuml;bernehmen. Sie hatten zwar zwei Kinder, aber
diese wandten sich von Reiki ab, da in ihrer Kindheit die Eltern st&auml;ndig wegen Reiki unterwegs waren.</p>

<p>Hawayo Takata (1900-1980) war Hawaiianerin japanischer Abstammung. Als sie schwer erkrankte und ihr Arzt ihr sagte, da&szlig; sie
nicht mehr lange zu leben habe, entschlo&szlig; sie sich 1935, nach Japan zu fahren. Ein japanischer Arzt vermittelte sie an die
Praxis von Dr. Hayashi. Nach zweimonatiger Reiki-Behandlung ging es ihr viel besser und in acht Monaten wurde sie vollst&auml;ndig
geheilt. Sie arbeitete und lernte ein Jahr bei Dr. Hayashi und ging dann wieder nach Hawaii zur&uuml;ck. Im Jahr 1938 besuchte
Dr. Hayashi sie in Hawaii und weihte sie zum Meistergrad. Zwei bis drei Jahre vor ihrem Tod begann sie ihrerseits, ihren
Sch&uuml;lern den Meistergrad zu verleiehn (d.h. sie hat etwa vierzig Jahre lang keinen zum Meistergrad geweiht). Insgesamt 22
Personen wurden von Hawayo Takata zum Meister geweiht. Durch diese 22 Meister wurde Reiki auf der ganzen Welt verbreitet,
und in zwanzig Jahren stieg die Zahl der Reiki-Praktizierenden auf &uuml;ber 5 Millionen.</p>

<p>Hawayo Takatas Enkelin Phyllis Lei Furumoto, eine der 22 Meister/innen, gr&uuml;ndete 1981 die "Reiki-Allianz". Eine andere
Meisterin, Barbara Rey, gr&uuml;ndete 1982 die "American International Reiki Association", die heute "Radiance Technic" hei&szlig;t.
Ab der zweiten H&auml;lfte der 1980er Jahre wurde Reiki vom Westen nach Japan reimportiert und dort langsam verbreitet. In Japan
gibt es heute ca. 50.000 Reiki-Praktizierende. Im Westen wurde gesagt, da&szlig; es in Japan keinen Nachfolger des Usui-Sensei mehr
gebe. Aber in den 1990er Jahren wurde durch Hiroshi Doi und Frank Petter bekannt, da&szlig; die von Usui-Sensei gegr&uuml;ndete
"Gesellschaft f&uuml;r die Usui-Reiki-Heilmethode" weiter existierte und heute noch existiert, bei der die traditionelle Reiki-Methode
nach Usui Sensei bewahrt wird.</p>




