<p>Tja, wo fange ich an? W&auml;re es mir m&ouml;glich in die Vergangenheit zu reisen und meinem fr&uuml;heren Selbst
zu sagen: "Hey, im Jahr 2006 wirst Du Dich zum Schamanen, respektive Inka-Priester initiieren lassen, Du wirst drei Tage lang
barfu&szlig; durch die schweizer Alpen laufen, wirst feststellen, dass es tats&auml;chlich mehr Dinge zwischen
Himmel und Erde gibt, als Du Dir jemals ertr&auml;umt hast, dass wir von Geistern umgeben sind und die
Magie tats&auml;chlich lebt und existiert und Du wirst mit Deinem Wissen und Deinen neuen F&auml;higkeiten anderen
Menschen helfen k&ouml;nnen Heilung zu erfahren."</p>
<p>Nunja, ich h&auml;tte mich wahrscheinlich schallend ausgelacht, mir den Vogel gezeigt und ganz stark
daran gezweifelt ob wir wirklich ein und dieselbe Person sind. Und wer k&ouml;nnte mir das schon ver&uuml;beln?
Ich meine Schamanismus? Geister? Energieheilung? Wer glaubt denn an so etwas!?</p>
<p>Ich habe jedenfalls nicht daran geglaubt! Mein Leben lang war ich ein recht n&uuml;chterner Mensch,
der mit beiden Beinen auf dem Boden der Tatsachen stand. Wahrheit sah ich nur in bewiesenen
Errungenschaften der Wissenschaft, jeder der mir mit Religion oder Esoterik kam, erhielt im besten
Falle von mir ein mildes L&auml;cheln und ein verst&auml;ndnisloses Kopfsch&uuml;tteln. Zugegeben, esoterische und
auch religi&ouml;se Themen haben mich schon immer fasziniert, mit Vorliebe habe ich Fantasy-Romane
verschlungen und Mysterie-Serien wie Akte-X geschaut, aber dennoch habe ich nicht eine Sekunde lang
an derartige Dinge geglaubt.</p>
<p>Aber die Zeiten &auml;ndern sich. Das Jahr 2005 war kein sch&ouml;nes Jahr, jedenfalls nicht f&uuml;r mich. Es gab
Tage, da erschien es mir fast, als w&auml;re jenes Jahr verflucht, sofern man an solche Dinge glauben
wollte. ;)</p>
<p>Es sah finanziell nicht besonders rosig aus f&uuml;r mich und meine Verlobte. Bei ihr wussten wir bis
zuletzt nicht, ob sie nach Ablauf ihres Zeitvertrages eine Verl&auml;ngerung bekommen w&uuml;rde oder nicht
(wie das ja in der heutigen Zeit leider normal zu sein scheint), ich selbst befand mich noch in meiner
Ausbildung und unsere Konten schienen leider nur noch rote Zahlen zu kennen. Hinzu kam dann noch, dass
mein Job mich nicht wirklich gl&uuml;cklich machte, er nicht das war, was ich mir vorgestellt hatte und
obendrein schien ich eine echte Pechstr&auml;hne erwischt zu haben.</p>
<p>Kennen Sie das? Man ist gerade eh nicht sonderlich gut bei Kasse und st&auml;ndig gehen einem Sachen
kaputt, was teure Reperaturen hinter sich herzieht? St&auml;ndig kommen neue Rechnungen und der Strafzettel
hat da auch noch gerade gefehlt! Die Tatsache, dass zwei Menschen aus meinem Bekanntenkreis in jenem
Jahr ihr Leben lie&szlig;en (ein tragischer Unfall und ein Selbstmord) trug auch nicht gerade zu einer
Verbesserung meines Gem&uuml;tszustandes bei.</p>
<p>Und &uuml;berhaupt: Ich blickte mich in meinem Freundeskreis um und allen schien es schlecht zu gehen.
Ich kann mich an kein Jahr erinnern, in dem derart viele Beziehungen in meinem Freundes- und
Bekanntenkreis in die Br&uuml;che gegangen sind wie im Jahr 2005. Und meine Liebste und ich stritten uns
nat&uuml;rlich auch &uuml;berdurchschnittlich h&auml;ufig, was sicher normal ist, wenn einem die Probleme und Sorgen
aus beiden Ohren heraus zu quillen scheinen.</p>
<p>Wie schon gesagt, es war kein wirklich gutes Jahr f&uuml;r mich. Und es gipfelte darin, dass ich kurz
davor stand in ernsthafte Depressionen zu verfallen, ja, zwischenzeitlich war ich wirklich sehr
depressiv, zum Gl&uuml;ck noch nicht im Endstadium. Ich hatte das bereits bei meinem Vater vor einigen
Jahren erlebt und wollte eigentlich nicht wirklich dasselbe durchmachen m&uuml;ssen wie er.</p>
<p>Dann kam der Tag, an dem ich durch "Zufall" einen interessanten Beitrag in einem Forum entdeckte.
Er trug den Titel "Inka-Schamanen" und war elend lang, aber doch sehr interessant und lesenswert. Wer
Interesse an diesem Text hat, kann ihn <a href="inka.php">hier</a> nachlesen. Da ich mich ja schon
immer f&uuml;r solche Dinge interessiert hatte, las ich den Text auch bis zum Ende durch.
Ich war sehr begeistert von dem Inhalt, auch wenn ich es f&uuml;r ziemlichen Humbug hielt. Nicht da&szlig; die
Qero die direkten Nachfahren der Inka waren, nein, auch nicht dass es ihnen anscheinend gelungen war
sich 500 Jahre lang in den Anden zu verstecken, aber die Geschichte mit der Energie-Medizin und dem
Heilen mit Licht fand ich dann doch schon viel zu bescheuert, als dass sie h&auml;tte wahr sein k&ouml;nnen.</p>
<p>Aber ich hatte Blut geleckt und da in dem Text ein Link zu einem weiteren Schamanen angegeben war,
folgte ich diesem Link und landete auf der <a href="http://www.shamanic-healing.de">Seite von Martin
Brune</a>. Dort gab (und gibt) es ein Ebook mit dem Titel "Vom Skeptiker zum Inka Schamanen"
zum kostenlosen Download. Ich lud die Datei also herunter und las mir die 14 Seiten von Herrn Brune
durch. Und wieder war ich v&ouml;llig fasziniert und gefangen von dem, was ich da las. Ich war nicht in
der Lage auch nur <i>ein Wort</i> davon zu glauben, aber es klang einfach zu gut! Ich wei&szlig; noch, wie
ich dachte: "Also entweder ist das tats&auml;chlich wahr und dieser Martin Brune hat das wirklich alles so
erlebt wie er hier schreibt oder aber er ist ein literarischer Meister und kann sich super verkaufen,
denn seine Geschichte liest sich irgendwie unglaublich authentisch."</p>
<p>Nunja, auf seiner Seite bot Martin jedenfalls ein sogenanntes "Einf&uuml;hrungsseminar" inklusive
"medialer Heilreise" (was immer das auch sein sollte) f&uuml;r 15 Euro an. Ich brauchte gar nicht lange
zu &uuml;berlegen. Kurzer Hand erz&auml;hlte ich meiner Verlobten und zwei unserer Freunde von dieser Sache und
schlug vor, zu dem n&auml;chsten Einf&uuml;hrungsseminar nach K&ouml;ln zu fahren. Gesagt, getan und ich ahnte zu
jenem Zeitpunkt noch nicht, dass dies mein Leben ma&szlig;geblich beeinflussen w&uuml;rde.</p>
<p>Das Seminar war recht interessant und unterhaltsam und ich glaubte weniger denn je daran, auch
wenn Martin von seiner Sache sehr &uuml;berzeugt schien. Nach dem theoretischen Teil des Seminars machten
wir uns dann alle an die mediale Heilreise, um einen Teil unserer Seele zur&uuml;ck zu gewinnen. Und
dabei geschah dann das merkw&uuml;rdige: Gegen Ende der Reise sp&uuml;rte ich Martin &uuml;ber mir stehen und er
blies mir mit einem festen Atemsto&szlig; gegen die Brust. Im n&auml;chsten Moment wurde mir unglaublich warm
in der Mitte der Brust und diese W&auml;rme verbreitete sich rasch in meinem ganzen K&ouml;rper. Ich konnte
es nicht fassen, bildete ich mir das etwa ein?</p>
<p>Es war doch gar nichts geschehen, au&szlig;er da&szlig; wir diese komische "Phantasiereise" gemacht hatten
und mich jemand angepustet hatte. Und dennoch hatte ich ein merkw&uuml;rdiges K&ouml;rpergef&uuml;hl. Auf dem
Heimweg nach dem Kurzseminar gab es f&uuml;r uns nur ein Thema: Wer was gesehen und gef&uuml;hlt hatte! Und
wir hatten ausnahmslos <b>alle</b> etwas gef&uuml;hlt und interessante Dinge w&auml;hrend der Heilreise
gesehen. Waren wir also alle verr&uuml;ckt? Massenhalluzination? Nein, das schien uns nun auch viel
zu abwegig. Vielleicht war an dem ganzen Energie- und Lichtgerede ja tats&auml;chlich etwas dran.
Die folgenden Tage und Wochen jedenfalls waren der Hammer!</p>
<p>Seit sehr langer Zeit tr&auml;umte ich wieder sehr intensiv und konnte mich auch an meine Tr&auml;ume
erinnern. Au&szlig;erdem war ich unglaublich fr&ouml;hlich und positiv gestimmt, von depressiven Gedanken
oder Gef&uuml;hlen keine Spur. Ich hatte meinen Optimismus zur&uuml;ckgewonnen, das Glas war nicht mehr
halb leer, sondern wieder halb voll! Meine Pechstr&auml;hne schien vorbei zu sein und dann und wann
winkte mir sogar das Gl&uuml;ck. Und was mich noch mehr &uuml;berraschte, ich hatte tats&auml;chlich etwas sehr
wertvolles wieder zur&uuml;ckbekommen, was ich bereits f&uuml;r verloren geglaubt hatte, meine empathische
Ader.</p>
<p>Ich habe mich schon immer gut in Menschen hineinversetzen und mit ihnen f&uuml;hlen k&ouml;nnen. Aber seit
einigen Jahren war diese Gabe immer mehr und mehr abgestumpft und schlie&szlig;lich v&ouml;llig verschwunden.
Nach der Heilreise jedoch, kam sie mit voller Kraft zur&uuml;ck und teilweise mit einer Wucht, dass es
schon wieder erschreckend war. So stark, dass ich tats&auml;chlich f&uuml;hlen konnte, wie sich mein Gegen&uuml;ber
f&uuml;hlt, seine Emotionen waren die meinen, das war schon fast unheimlich.</p>
<p>Seit dem gab es dann f&uuml;r mich kein Halten mehr. Ich besorgte mir alles an Literatur, was ich
&uuml;ber Schamanismus in die Finger bekommen konnte und las wie ein Besessener. Ich f&uuml;hlte mich
seltsam mit dieser Thematik verbunden, immer wenn ich mich damit besch&auml;ftigte lachte mein Herz auf
und meine Seele begann zu klingen. Ich, der gro&szlig;e Realist, konnte f&uuml;hlen dass es meine
Berufung war mich mit dem Schamanismus zu besch&auml;ftigen, ja mehr noch, es auch zu lernen. Und das
tat ich dann auch. Zuerst versuchte ich mich an den zahlreichen &Uuml;bungen in den B&uuml;chern, aber das
war nicht so ganz mein Fall. Ich war schon immer ein Mensch gewesen, der am besten von anderen
Menschen lernt und nicht aus B&uuml;chern. Ich schrieb dem Verfasser des oben erw&auml;hnten Textes &uuml;ber
Inka-Schamanen eine Email und stellte ihn ein paar Fragen. Es stellte sich heraus, dass dieser
Jemand au&szlig;erordentlich offenherzig mir gegen&uuml;ber war und mich immer weiter dazu ermutigte nicht
aufzugeben und weiter zu trainieren. Auch gab er mir Tipps f&uuml;r Meditations- und Wahrnehmungs&uuml;bungen
und ich &uuml;bte mich auch in der schamanischen Reise.</p>
<p>Ich wurde immer besser und freute mich &uuml;ber jeden noch so kleinen Erfolg, und als es dann im
Juni 2006 dazu kam dass mein Mentor, den ich bis dato nur durch Emailverkehrt kannte, ein Seminar
in der Schweiz gab, war ich sofort Feuer und Flamme. Zum Einen freute ich mich sehr darauf diesen
Menschen, der mir charakterlich so unglaublich &auml;hnlich war endlich pers&ouml;nlich kennen zu lernen und
zum Anderen wollte ich die Chance auf eine Initation auf keinen Fall verpassen. Gemeinsam mit sechs
weiteren Teilnehmern unterrichtete uns Andre Hirschmann also im Schamanismus der Anden.</p>
<p>Und ich kann, ohne auch nur im Mindesten zu &uuml;bertreiben, behaupten dass diese drei Tage alles
zuvor gelernte und erlebte v&ouml;llig in den Schatten stellten und zu der unglaublichsten Erfahrung
meines Lebens wurden! Aber das ist eine andere, noch l&auml;ngere Geschichte und ich m&ouml;chte hier niemanden
mit elend langen Texten zu sehr langweilen. <a href="sandro_noch_mehr.php#noch_mehr">&rArr Ich will's aber dennoch lesen!</a></p>
<p>So kam ich also zum Schamanismus und ich lerne jeden Tag dazu, denn die besten Lehrer sind die
Spirits selbst, wenn man erst einmal soweit ist sie wahrzunehmen und ihnen zuzuh&ouml;ren. ;)
Vom 25. September bis zum 30. September 2006 nahm ich an einem Kurs &uuml;ber Inka-Schamanismus von Elizabeth Jenkins
teil. In dieser einen Woche lernte ich noch einmal unglaublich viel, vertiefte mein Wissen &uuml;ber die Qero und lernte
den Unterschied zwischen Schamanismus und Andinen Priester kennen. Der Unterschied ist sehr fein, aber mittlerweile
wei&szlig; ich nun, da&szlig; es nicht richtig ist mich als Schamanen zu bezeichnen, in Wirklichkeit bin ich ein Priester der
Erde. Diese Woche mit Elizabeth war sehr transformierend und verbesserte meine F&auml;higkeiten und st&auml;rkte meinen Energiek&ouml;rper.
Am meisten freute ich mich dar&uuml;ber, anschlie&szlig;end zu Andre zu fahren und ihm alles Neue beizubringen, denn auf diese Weise
erf&uuml;llte ich nicht nur den Grundsatz des <i>ayni</i>, sondern war auch endlich in der gl&uuml;cklichen Lage, ihm etwas von dem
zur&uuml;ck zu geben, was ich von ihm gelernt habe.</p>

