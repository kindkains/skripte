<p>Hiroko Kasahara und Josef Hilger (Jupp) sind unsere Reiki Lehrer. Wir haben von ihnen den Shoden Grad in Gendai Reiki-h&ocirc;
erhalten. Josef Hilger wurde 1988 von Phyllis Furumoto zum Reiki Meister der westlichen Reikimethode "Usui-Shiki, Usui System"
ausgebildet und ist somit ein Lehrer der vierten Generation (von Mikao Usui aus gez&auml;hlt). Siehe hierzu auch das Diagramm der
<a href="../../download/inka/linie.pdf">&Uuml;bertragungslinie</a>. Dar&uuml;ber hinaus ist er ein &uuml;beraus begabter Heilpraktiker
und ein "&auml;scht k&ouml;lscher Jung!"</p>

<p>Hiroko Kasahara erhielt im Jahr 2000 von Josef eine Reikiausbildung (den ersten und zweiten Grad.). Anschlie&szlig;end begann sie
intensiv Nachforschungen in Japan zu betreiben, da Reiki ja urspr&uuml;nglich aus Japan stammt und von Mikao Usui entwickelt wurde.
So machte sie die Bekanntschaft mit Hiroshi Doi, dem Entwickler von Gendai Reiki-h&ocirc; und dem Gr&uuml;nder der Gendai Reiki Healing
Association.</p>

<p>Auch Hiroshi Doi hatte seinerzeit zuerst die westliche Reikimethode erlernt und begann dann Nachforschungen in Japan anzustellen.
Damals (und manchmal auch noch heute) wurde f&auml;lschlicherweise davon ausgegangen, da&szlig; die urspr&uuml;ngliche, japanische Reiki Methode
verloren gegangen sei und es nur noch das westliche Reiki g&auml;be. Selbst in Japan war Reiki damals noch relativ unbekannt und wenn
&uuml;berhaupt so fand man nur das westliche Reiki vor. Doch Hiroshi Doi hatte Gl&uuml;ck durch Zufall einem Mitglied der Usui Reiki Ry&ocirc;h&ocirc;
Gakkai, der <i>urspr&uuml;nglichen, von Mikao Usui gegr&uuml;ndeten,</i> Gesellschaft &uuml;ber den Weg zu laufen und in die Usui Reiki Ry&ocirc;h&ocirc; Gakkai
aufgenommen zu werden. Hiroshi war sehr erstaunt dar&uuml;ber, wie sehr sich die urspr&uuml;nglich japanische und die westliche Reiki Methode
voneinander unterschieden, erkannte jedoch in beiden Methoden jeweils Vor- und Nachteile. So entwickelte er schlie&szlig;lich Gendai
Reiki-h&ocirc; und gr&uuml;ndete seine Gendai Reiki Healing Association. H&ocirc; bedeutet "Methode" und Gendai hei&szlig;t soviel wie "Modern", &uuml;bersetzt
ist dies also die Moderne Reiki Methode, welche die jeweiligen Vorteile des westlichen und des japanischen Reiki ineinander vereint.
Hiroko wurde also von Hiroshi Doi in den Meister/Lehrergrad des Gendai Reiki-h&ocirc; eingeweiht und ist somit, ebenso wie Jupp, eine
Reiki Meisterin der vierten Generation. (Siehe <a href="../../download/inka/linie.pdf">&Uuml;bertragungslinie</a>)</p>

<p>Josef Hilger hatte in all seinen Jahren mit seinen Reikierfahrungen immer das Gef&uuml;hl gehabt, da&szlig; <i>irgendetwas</i> fehlt.
Als Hiroko zur&uuml;ck aus Japan kam und ihm von Gendai Reiki-h&ocirc; erz&auml;hlte und es ihm demonstrierte, hatte er endlich diesen "fehlenden Teil"
gefunden und bat Hiroko darum seine Lehrerin zu werden. Seit dem (2003) lehren die beiden zusammen in K&ouml;ln die Gendai Reiki-h&ocirc; vielen
interessierten Menschen, unter anderem auch sehr erfolgreich bereits fertig ausgebildeten Reiki Meistern, da auch diese bemerkt haben,
da&szlig; ihnen im westlichen Reiki <i>etwas</i> fehlt. N&auml;here Informationen findet man auf ihrer Website der
<a href="http://www.reikischulekoeln.de/">Reikischule K&ouml;ln</a>.</p>

